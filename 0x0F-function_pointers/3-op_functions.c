#include "3-calc.h"

/**
 * op_add - the sum of two numbers.
 * @a: first number.
 * @b: second number.
 *
 * Return: The sum of two numbers.
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - substraction of two numbers.
 * @a: first number.
 * @b: second number.
 *
 * Return: difference of two.
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplication of two numbers.
 * @a: first number.
 * @b: second number.
 *
 * Return: results of multiplication
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division of two numbers.
 * @a: first number.
 * @b: second number.
 *
 * Return: result of division.
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - remainder of division.
 * @a: first number.
 * @b: second number.
 *
 * Return: remainder after division.
 */
int op_mod(int a, int b)
{
	return (a % b);
}


